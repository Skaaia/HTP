package lesson1;

public class Task15 {
    public static void main(String[] args) {
        int c1=1;
        int c2=1;
        for(int i=1;i<=100; i++){
            System.out.println(i);
        }
        while (c1<=100){
            System.out.println(c1);
            c1++;
        }
        do{
            System.out.println(c2);
            c2++;
        }while (c2<=100);
    }
}
